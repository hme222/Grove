def lookup_zone(country: str, postcode: str) -> Tuple[Optional[str], str, Optional[str]]:
    """Return (zone, system, source_label).

    system is "USDA" for US/PR/HI, "RHS" for UK, "" if unknown.
    source_label describes how the zone was determined ("zip-prefix",
    "state-default", "postcode-area", "manual" or "" if not found).
    """
    if not postcode:
        return None, "", None
    pc = postcode.strip().upper().replace(" ", "")
    country = (country or "").strip().upper()
    if country in ("US", "USA", "UNITED STATES", ""):
        # Default to US if not specified — US-only fallback to keep UX simple.
        prefix = pc[:3]
        if prefix in US_ZIP_PREFIX_TO_ZONE:
            return US_ZIP_PREFIX_TO_ZONE[prefix], "USDA", "zip-prefix"
        state = _state_for_prefix(prefix)
        if state and state in US_STATE_DEFAULT_ZONE:
            return US_STATE_DEFAULT_ZONE[state], "USDA", "state-default"
        return None, "USDA", None
    if country in ("UK", "GB", "UNITED KINGDOM", "GREAT BRITAIN"):
        # UK postcodes start with 1 or 2 letters, then digits — extract the area.
        area = ""
        for ch in pc:
            if ch.isalpha():
                area += ch
            else:
                break
        # Try 2-letter then 1-letter
        if area[:2] in UK_POSTCODE_AREA_TO_RHS:
            return UK_POSTCODE_AREA_TO_RHS[area[:2]], "RHS", "postcode-area"
        if area[:1] in UK_POSTCODE_AREA_TO_RHS:
            return UK_POSTCODE_AREA_TO_RHS[area[:1]], "RHS", "postcode-area"
        return None, "RHS", None
    return None, "", None
